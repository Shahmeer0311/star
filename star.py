import turtle
turtle.Screen().bgcolor("yellow")
turtle.shape("turtle")
x = turtle.Turtle()
x.forward(100)
x.left(120)
x.forward(100)
x.left(120)
x.forward(100)
x.penup()
x.right(150)
x.forward(50)
x.pendown()
x.right(90)
x.forward(100)
x.right(120)
x.forward(100)
x.right(120)
x.forward(100)
turtle.done()